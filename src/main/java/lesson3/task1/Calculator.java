package lesson3.task1;

public class Calculator {


    int summ(int a, int b) {
        int sum = a + b;
        System.out.println(sum);
        return sum;
    }

    int substract(int a, int b) {
        int diff = a - b;
        System.out.println(diff);
        return diff;

    }

    int multiply(int a, int b) {
        int sum = a * b;
        System.out.println(sum);
        return sum;
    }

    int divide(int a, int b) {
        int sum = a / b;
        System.out.println(sum);
        return sum;
    }
}
